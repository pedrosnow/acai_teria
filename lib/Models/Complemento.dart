class Complemento {
  int _idComplemento;
  String _nome;
  dynamic _boleano;

  Complemento();

  get idComplemento => this._idComplemento;

  set idComplemento(value) => this._idComplemento = value;

  get nome => this._nome;

  set nome(value) => this._nome = value;

  get boleano => this._boleano;

  set boleano(value) => this._boleano = value;
}
